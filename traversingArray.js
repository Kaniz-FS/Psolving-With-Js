// How to get all the elements in array with for loop
const fruits = ["mango", "banana", "apple", "guava"];

for (let i = 0; i < fruits.length; i++) {
  console.log(fruits[i]);
}

// How to get all the elements in array with while loop
const colour = ["mauve", "emerald", "crimson", "olive"];
let j = 0;
while (j < colour.length) {
  console.log(colour[j]);
  j++;
}
